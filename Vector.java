

import java.lang.Math;
 class Vector{
   public double x;
   public double y;
   
   public Vector(){
      x=0;
      y=0;
      
   }
   /**
    * @param x x value
   * @param y y value
  */
   public Vector(double x,double y){
      this.x=x;
      this.y=y;
     
   }
   
  



}